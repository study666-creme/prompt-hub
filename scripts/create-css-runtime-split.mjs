import { mkdir, writeFile } from 'node:fs/promises';
import { existsSync, readFileSync } from 'node:fs';
import { basename, join, relative } from 'node:path';
import { dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

const entry = process.argv[2];
const outDir = process.argv[3];
const targetLines = Number(process.argv[4] || 700);

if (!entry || !outDir) {
  console.error('Usage: node scripts/create-css-runtime-split.mjs <entry-css> <out-dir> [target-lines]');
  process.exit(1);
}
if (!Number.isFinite(targetLines) || targetLines < 100) {
  throw new Error(`Invalid target-lines: ${process.argv[4]}`);
}

const entryPath = join(root, entry);
if (!existsSync(entryPath)) throw new Error(`Missing CSS entry: ${entry}`);
const source = readFileSync(entryPath, 'utf8').replace(/^\uFEFF/, '');
if (source.includes('__PROMPT_HUB_CSS_SPLIT_MANIFEST__')) {
  throw new Error(`${entry} already looks like a CSS split manifest`);
}

const build = (readFileSync(join(root, 'index.html'), 'utf8').match(/__APP_BUILD__\s*=\s*'([^']+)'/) || [])[1] || 'dev';
const lines = source.split(/\r?\n/);
const chunks = [];
let buf = [];
let depth = 0;
let inBlockComment = false;

function updateDepth(line) {
  let i = 0;
  while (i < line.length) {
    if (inBlockComment) {
      const end = line.indexOf('*/', i);
      if (end < 0) return;
      inBlockComment = false;
      i = end + 2;
      continue;
    }
    if (line.startsWith('/*', i)) {
      inBlockComment = true;
      i += 2;
      continue;
    }
    const ch = line[i];
    if (ch === '{') depth += 1;
    else if (ch === '}') depth = Math.max(0, depth - 1);
    i += 1;
  }
}

for (const line of lines) {
  buf.push(line);
  updateDepth(line);
  if (buf.length >= targetLines && depth === 0 && !inBlockComment) {
    chunks.push(buf.join('\n'));
    buf = [];
  }
}
if (buf.length) chunks.push(buf.join('\n'));

const absOutDir = join(root, outDir);
await mkdir(absOutDir, { recursive: true });
const width = String(chunks.length).length < 2 ? 2 : String(chunks.length).length;
const relParts = [];
for (let i = 0; i < chunks.length; i += 1) {
  const name = `part-${String(i + 1).padStart(width, '0')}.css`;
  const rel = relative(root, join(absOutDir, name)).replace(/\\/g, '/');
  relParts.push(rel);
  await writeFile(join(absOutDir, name), chunks[i] + (chunks[i].endsWith('\n') ? '' : '\n'), 'utf8');
}

const manifest = [
  `/* __PROMPT_HUB_CSS_SPLIT_MANIFEST__ ${basename(entry)}`,
  ' * Generated by scripts/create-css-runtime-split.mjs.',
  ' * Keep imports in this exact order; the split preserves cascade order.',
  ' */',
  ...relParts.map((rel) => `@import url("${rel}?v=${build}");`),
  ''
].join('\n');

await writeFile(entryPath, manifest, 'utf8');
console.log(`split ${entry} into ${chunks.length} CSS chunks under ${outDir}`);
