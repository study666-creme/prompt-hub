import { mkdir, writeFile } from 'node:fs/promises';
import { existsSync, readFileSync } from 'node:fs';
import { basename, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';
import { dirname } from 'node:path';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

const entry = process.argv[2];
const outDir = process.argv[3];
const linesPerChunk = Number(process.argv[4] || 900);

if (!entry || !outDir) {
  console.error('Usage: node scripts/create-legacy-runtime-split.mjs <entry-file> <out-dir> [lines-per-chunk]');
  process.exit(1);
}
if (!Number.isFinite(linesPerChunk) || linesPerChunk < 100) {
  throw new Error(`Invalid lines-per-chunk: ${process.argv[4]}`);
}

const entryPath = join(root, entry);
if (!existsSync(entryPath)) throw new Error(`Missing entry file: ${entry}`);

const source = readFileSync(entryPath, 'utf8').replace(/^\uFEFF/, '');
if (source.includes('__PROMPT_HUB_LEGACY_SPLIT_LOADER__')) {
  throw new Error(`${entry} already looks like a legacy split loader`);
}

const lines = source.split(/\r?\n/);
const parts = [];
for (let i = 0; i < lines.length; i += linesPerChunk) {
  parts.push(lines.slice(i, i + linesPerChunk).join('\n'));
}

const absOutDir = join(root, outDir);
await mkdir(absOutDir, { recursive: true });

const width = String(parts.length).length < 2 ? 2 : String(parts.length).length;
const partPaths = [];
for (let i = 0; i < parts.length; i += 1) {
  const name = `part-${String(i + 1).padStart(width, '0')}.js`;
  const rel = relative(root, join(absOutDir, name)).replace(/\\/g, '/');
  partPaths.push(rel);
  const suffix = i === parts.length - 1 && parts[i].endsWith('\n') ? '' : '\n';
  await writeFile(join(absOutDir, name), parts[i] + suffix, 'utf8');
}

const entryName = basename(entry);
const loader = `/* __PROMPT_HUB_LEGACY_SPLIT_LOADER__ ${entryName}
 * Generated by scripts/create-legacy-runtime-split.mjs.
 * The runtime still executes synchronously so downstream classic scripts keep
 * the same ordering and closure behavior as the former monolithic file.
 */
(function () {
  'use strict';
  var parts = ${JSON.stringify(partPaths, null, 2).replace(/\n/g, '\n  ')};
  var current = document.currentScript;
  var baseUrl = current && current.src ? current.src : '${entryName}';
  var version = (baseUrl.match(/[?&]v=([^&]+)/) || [])[1] || window.__APP_BUILD__ || String(Date.now());
  var code = '';
  for (var i = 0; i < parts.length; i += 1) {
    var url = new URL(parts[i] + '?v=' + encodeURIComponent(version), baseUrl).href;
    var xhr = new XMLHttpRequest();
    xhr.open('GET', url, false);
    xhr.send(null);
    if ((xhr.status && (xhr.status < 200 || xhr.status >= 300)) || !xhr.responseText) {
      throw new Error('Failed to load ${entryName} chunk: ' + parts[i] + ' (' + xhr.status + ')');
    }
    code += xhr.responseText + '\\n';
  }
  (0, eval)(code + '\\n//# sourceURL=${entryName}.legacy-runtime.js');
})();
`;

await writeFile(entryPath, loader, 'utf8');
console.log(`split ${entry} into ${parts.length} chunks under ${outDir}`);
